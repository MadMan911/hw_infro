def search_faq(query: str) -> str:
    """Search the FAQ knowledge base by keyword matching."""
    query_lower = query.lower()
    query_words = query_lower.split()

    scored = []
    for entry in FAQ_DATABASE:
        score = 0
        text = (entry["question"] + " " + entry["answer"]).lower()
        tags = [t.lower() for t in entry["tags"]]

        for word in query_words:
            if len(word) < 3:
                continue
            if word in tags:
                score += 3
            elif word in text:
                score += 1

        if score > 0:
            scored.append((score, entry))

    scored.sort(key=lambda x: x[0], reverse=True)
    top = scored[:3]

    if not top:
        return "По запросу ничего не найдено в базе знаний."

    results = []
    for _, entry in top:
        results.append(f"В: {entry['question']}\nО: {entry['answer']}")
    return "\n\n".join(results)


